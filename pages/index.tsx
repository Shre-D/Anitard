import Head from "next/head";
import Header from "../components/Header";
import { useState, useEffect } from "react";
import HomeDisplay from "../components/HomeDisplay";

export default function Home() {
  const [animeList, setAnimeList] = useState([]);
  const [search, setSearch] = useState("");

  const getSearchResult = async (query: string) => {
    const searchResult = await fetch(
      `https://api.jikan.moe/v4/anime?q=${query}&limit=10
      `
    ).then((res) => res.json());
    setAnimeList(searchResult.data);
  };

  const HandleSearch = (e: any) => {
    e.preventDefault();
    getSearchResult(search);
  };

  return (
    <div className="">
      <Head>
        <title>Anitard</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/mylifeamovie.png" />
      </Head>
      <Header />
      <main className="pb-24">
      <div className="pt-24 pl-[66vh] md:pt-24 md:pl-[20vh]">
        <div className="transition-1000">
          <h1 className="pl-28 text-8xl font-medium font-manrope text-transparent bg-clip-text bg-gradient-to-r from-blue-600 to-pink-300 md:pl-2 hover:white">
            ANITARD
          </h1>
        </div>
        <div className="flex items-center">
          <form className="pb-10" onSubmit={HandleSearch}>
            <input
              type="text"
              placeholder="🔍 Search here..."
              className="flex transition-200 pl-4 bg-white text-black text-justify font-semibold items-center absolute rounded-3xl h-14 w-[92vh] hover:bg-gray-200"
              value={search}
              onChange={(e) => setSearch(e.target.value)}
              required
            />
          </form>
          <div className="pt-36 pl-32">
            One place to find your favourite manga and anime!
          </div>
        </div>
      </div>
      <div className="flex justify-center gap-14 pl-10 pt-16">
        <img
          src="show4.png"
          alt="guts"
          className="h-48 w-48 rounded-full transition:1000 hover:scale-105 hover:opacity-0 md:invisible"
        />
        <img
          src="show1.png"
          alt="otonari"
          className="h-48 w-48 rounded-full transition:1000 hover:scale-105 hover:opacity-0"
        />
        <img
          src="show2.png"
          alt="mylifeamovie"
          className="h-48 w-48 rounded-full transition:1000 hover:scale-105 hover:opacity-0"
        />
        <img
          src="show3.png"
          alt="animelogo"
          className="h-48 w-48 rounded-full transition:1000 hover:scale-105 hover:opacity-0 "
        />
        <img
          src="show5.png"
          alt="Bam"
          className="h-48 w-48 rounded-full transition:1000 hover:scale-105 hover:opacity-0 md:invisible"
        />
      </div>
      </main>
     
      <HomeDisplay
        HandleSearch={HandleSearch}
        search={search}
        setSearch={setSearch}
        animeList={animeList}
      />
    </div>
  );
}
